%--------------------------------------------------------------------------
% Minimizing a function graphically by inspection
% minimize   f(x) = x1^2 + x2 + 4
% subject to c1(x) = -x1^2 - (x2 + 4)^2 + 16 >= 0
%            c2(x) = x1 - x2 - 6 >= 0
%--------------------------------------------------------------------------
% Setup the problem. Hold should be on. Minor grid axis is on. Aspect
% ratio is 1 to 1 to 1.
%--------------------------------------------------------------------------
figure(1);
pbaspect([1 1 1]);
hold on;
grid on;
grid minor;
%--------------------------------------------------------------------------
% plot c1(x) = x1^2 + (x2 + 4)^2 = 16
% done by hand, a circle of radius 4 centered at [0 -4]
%--------------------------------------------------------------------------
x = 0;
y = -4;
r = 4;
th = 0:pi/50:2*pi;
xunit = r * cos(th) + x;
yunit = r * sin(th) + y;
plot(xunit, yunit);
%--------------------------------------------------------------------------
% plot c2(x) = x2 = x1 - 6
%--------------------------------------------------------------------------
x1 = [-10:1:10];
x2 = x1 - 6;
plot (x1, x2);
%--------------------------------------------------------------------------
% plot the parabola for the function f(x) = x2 = -x1^2 - 4
%--------------------------------------------------------------------------
fx1 = [-10:1:10];
f = -(fx1).^2 - 4;
plot (fx1, f);
%--------------------------------------------------------------------------
% plot the shifted parabola for the function f(x) = x2 = -x1^2 - 4 + c
%--------------------------------------------------------------------------
c = -4;
fx2 = [-10:1:10];
f2 = -(fx2).^2 - 4 + c;
plot (fx2, f2);
hold off